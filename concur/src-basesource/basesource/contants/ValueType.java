package basesource.contants;

/**
 * 值类型
 * @author Jake
 *
 */
public interface ValueType {
	
	/**
	 * 单个值
	 */
	String VALUE = "{value}";
	
	/**
	 * json值
	 */
	String JSON_VALUE = "{jsonValue}";
	
	/**
	 * 用|隔开的值列表
	 */
	String SPLIT_VALUE = "{splitValue}";
	
	/**
	 * 用_隔开的值
	 */
	String ATTR_VALUE = "{attrValue}";
	
}
